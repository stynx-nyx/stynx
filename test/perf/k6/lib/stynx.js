function jsonHeaders(extra = {}) {
  return Object.assign({ 'content-type': 'application/json' }, extra);
}

function serializeMetrics(metrics) {
  const serialized = {};
  Object.keys(metrics).forEach((name) => {
    const metric = metrics[name];
    serialized[name] = metric && metric.values ? metric.values : {};
  });
  return serialized;
}

export function headerNumber(response, name) {
  const headers = response && response.headers ? response.headers : {};
  const exact = headers[name];
  const lowered = headers[name.toLowerCase()];
  const raw = exact !== undefined && exact !== null ? exact : lowered;
  const value = Array.isArray(raw) ? raw[0] : raw;
  const parsed = Number(value);
  return Number.isFinite(parsed) ? parsed : null;
}

function metricValue(response, name) {
  const headerValue = headerNumber(response, name);
  return headerValue !== null ? headerValue : response.timings.duration;
}

function jsonNumber(response, path) {
  try {
    const value = response.json(path);
    return typeof value === 'number' && Number.isFinite(value) ? value : null;
  } catch (_error) {
    return null;
  }
}

export function handleSummary(data) {
  return {
    stdout: `${JSON.stringify({ metrics: serializeMetrics(data.metrics) }, null, 2)}\n`,
    [SUMMARY_PATH]: JSON.stringify(data, null, 2),
  };
}

export function login() {
  const response = http.post(
    `${BASE_URL}/_reference/dev-login`,
    JSON.stringify({
      email: EMAIL,
      tenantId: TENANT_ID,
    }),
    {
      headers: jsonHeaders(),
      tags: { name: 'dev-login' },
    },
  );

  check(response, {
    'dev-login status 201': (r) => r.status === 201,
    'dev-login has access token': (r) => Boolean(r.json('accessToken')),
  }) || fail(`dev-login failed: ${response.status} ${response.body}`);

  return response.json('accessToken');
}

