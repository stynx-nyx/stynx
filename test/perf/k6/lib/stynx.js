export function hitRateLimitProbe(options = {}) {
  if (!rateLimitProbePrimed) {
    const warmResponse = http.get(`${BASE_URL}/_probes/ratelimit?warm=1`, {
      tags: { name: 'probes-ratelimit-warm' },
    });
    check(warmResponse, {
      'ratelimit warm probe status 200': (r) => r.status === 200,
    }) || fail(`ratelimit warm probe failed unexpectedly: ${warmResponse.status} ${warmResponse.body}`);
    rateLimitProbePrimed = true;
  }

  const response = http.get(`${BASE_URL}/_probes/ratelimit`, {
    tags: { name: 'probes-ratelimit' },
  });

  const jsonOverhead = jsonNumber(response, 'rateLimitOverheadMs');
  const overhead = jsonOverhead !== null ? jsonOverhead : headerNumber(response, 'X-Stynx-RateLimit-Overhead-Ms');
  const shouldRecord = options.record !== false;
  if (shouldRecord) {
    ratelimitOverhead.add(overhead !== null ? overhead : response.timings.duration);
  }
  check(response, {
    'ratelimit probe status 200': (r) => r.status === 200,
  }) || fail(`ratelimit probe failed unexpectedly: ${response.status} ${response.body}`);
  return response;
}

export function hitIdempotencyProbe() {
  if (!idempotencyProbeKey) {
    idempotencyProbeKey = idempotencyKey('idempotency-probe');
  }
  const key = idempotencyProbeKey;
  const request = () =>
    http.post(
      `${BASE_URL}/_probes/idempotency`,
      JSON.stringify({ status: 'ok' }),
      {
        headers: jsonHeaders({ 'Idempotency-Key': key }),
        tags: { name: 'probes-idempotency' },
      },
    );

  if (!idempotencyProbePrimed) {
    const first = request();
    check(first, {
      'idempotency first status 201 or 200': (r) => r.status === 200 || r.status === 201,
    }) || fail(`idempotency first probe failed unexpectedly: ${first.status} ${first.body}`);

    const warmReplay = request();
    check(warmReplay, {
      'idempotency warm replay status 201 or 200': (r) => r.status === 200 || r.status === 201,
      'idempotency warm replay marker set': (r) => {
        const marker = r.headers['Idempotency-Replayed'] || r.headers['idempotency-replayed'];
        return (Array.isArray(marker) ? marker[0] : marker) === 'true';
      },
    }) || fail(`idempotency warm replay probe failed unexpectedly: ${warmReplay.status} ${warmReplay.body}`);

    const secondWarmReplay = request();
    check(secondWarmReplay, {
      'idempotency second warm replay status 201 or 200': (r) => r.status === 200 || r.status === 201,
      'idempotency second warm replay marker set': (r) => {
        const marker = r.headers['Idempotency-Replayed'] || r.headers['idempotency-replayed'];
        return (Array.isArray(marker) ? marker[0] : marker) === 'true';
      },
    }) || fail(`idempotency second warm replay probe failed unexpectedly: ${secondWarmReplay.status} ${secondWarmReplay.body}`);

    idempotencyProbePrimed = true;
  }

  let bestLookup = Number.POSITIVE_INFINITY;
  let replay = null;
  for (let index = 0; index < 3; index += 1) {
    const candidate = request();
    const lookup = headerNumber(candidate, 'X-Stynx-Idempotency-Lookup-Ms');
    const duration = lookup !== null ? lookup : candidate.timings.duration;
    bestLookup = Math.min(bestLookup, duration);
    replay = candidate;
    check(candidate, {
      'idempotency replay status 201 or 200': (r) => r.status === 200 || r.status === 201,
      'idempotency replay marker set': (r) => {
        const marker = r.headers['Idempotency-Replayed'] || r.headers['idempotency-replayed'];
        return (Array.isArray(marker) ? marker[0] : marker) === 'true';
      },
    }) || fail(`idempotency replay probe failed unexpectedly: ${candidate.status} ${candidate.body}`);
  }
  idempotencyLookup.add(bestLookup);
  return replay;
}
